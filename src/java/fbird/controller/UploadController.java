    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String url = null;
        try {
            Part part = request.getPart("productImage");
            String realPath = request.getServletContext().getRealPath("/img");
            String nameImage = Paths.get(part.getSubmittedFileName()).getFileName().toString();
            if (!Files.exists(Paths.get(realPath))) {
                Files.createDirectory(Paths.get(realPath));
            }
            part.write(realPath + "/" + nameImage);
            String productImage = "img" + "/" + nameImage;
            if (productImage != null) {
                ProductDAO dao = new ProductDAO();
                int shopProductID = dao.getID1();
                boolean check = dao.addImageProduct(shopProductID, productImage);
                if(check){
                    url = "ViewCreateProductController";
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            request.getRequestDispatcher(url).forward(request, response);
        }
    }

